
public class Movie {
	String name, type, performers, review;
	int releaseYear;
	Double rating;
	
	public Movie(String name, String type, String performers, int releaseYear, Double rating, String review) {
		this.name = name;
		this.type = type;
		this.performers = performers;
		this.releaseYear = releaseYear;
		this.rating = rating;
		this.review = review;
		
	}
	

}
